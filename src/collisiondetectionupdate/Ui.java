    @Override
    public void keyPressed(KeyEvent e) {
        switch (e.getKeyCode()) {
            case KeyEvent.VK_UP:
                if (Y < 1) {
                    return;
                }
                Y--;
                break;
            case KeyEvent.VK_DOWN:
                if (Y > FRAME_SIZE.getHeight() - 90) {
                    return;
                }
                Y++;
                break;
            case KeyEvent.VK_LEFT:
                if (X < 1) {
                    return;
                }
                X--;
                break;
            case KeyEvent.VK_RIGHT:
                if (X > FRAME_SIZE.getWidth() - 66) {
                    return;
                }
                X++;
                break;
            default:
                break;
        }
        updateLocation(X, Y);
    }

    @Override
    public void keyReleased(KeyEvent e) {

    }

