    @Override
    public void update(Graphics g) {
        Graphics2D G2 = (Graphics2D) g;

        if (FIRST_RUN) {
            AREA_CLEAR = new Rectangle(FRAME_SIZE);
            BUFFERED_IMAGE = (BufferedImage) createImage(FRAME_SIZE.width, FRAME_SIZE.height);
            G2D = BUFFERED_IMAGE.createGraphics();
            R1.setLocation(X, Y);
            R2.setLocation(((int) FRAME_SIZE.getWidth() / 2) - 100 / 2, ((int) FRAME_SIZE.getHeight() / 2) - 100 / 2);
            FIRST_RUN = false;
        }

        G2D.setColor(Color.white);
        G2D.clearRect(0, 0, AREA_CLEAR.width, AREA_CLEAR.height);

        G2D.setPaint(Color.RED);
        G2D.draw(R1);
        G2D.setPaint(Color.RED);
        G2D.fill(R1);

        G2D.setPaint(Color.BLUE);
        G2D.draw(R2);
        G2D.setPaint(Color.BLUE);
        G2D.fill(R2);

        if (R1.intersects(R2)) {
            G2D.setColor(Color.BLACK);
            G2D.setFont(new Font("default", Font.BOLD, 16));
            G2D.drawString("collision !", 150, 50);
        } else {

        }

        G2.drawImage(BUFFERED_IMAGE, 0, 0, this);

    }

    public void updateLocation(int X, int Y) {
        R1.setLocation(X, Y);
        repaint();
    }
